package com.java.exercises.practice.solution.basic_part1.ex6;

public class TwoNumbers {
    public static void main(String[] args) {
        int a = 125;
        int b = 24;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
