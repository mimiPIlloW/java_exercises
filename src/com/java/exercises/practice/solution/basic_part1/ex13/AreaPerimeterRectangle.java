package com.java.exercises.practice.solution.basic_part1.ex13;

public class AreaPerimeterRectangle {
    public static void main(String[] args) {
        double widthRectangle = 5.6;
        double heightRectangle = 8.5;
        double areaRectangle = widthRectangle * heightRectangle;
        double perimeterRectangle = 2 * (widthRectangle + heightRectangle);

        System.out.println(areaRectangle);
        System.out.println(perimeterRectangle);
    }
}
